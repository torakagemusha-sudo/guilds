def _phase_to_const(phase_name: str, default: str = "execute") -> str:
    """Map human phase names to evaluator constants."""
    return PHASE_NAME_TO_CONST.get(phase_name, PHASE_NAME_TO_CONST[default])


def _collect_declarations(program):
    """Normalize AST declarations into typed lists."""
    buckets = {
        "claims": [],
        "affords": [],
        "vessels": [],
        "stages": [],
        "flows": [],
        "bonds": [],
        "seams": [],
    }

    for decl in getattr(program, "declarations", []):
        kind = decl.__class__.__name__
        if kind == "ClaimDecl":
            buckets["claims"].append(decl)
        elif kind == "AffordDecl":
            buckets["affords"].append(decl)
        elif kind == "VesselDecl":
            buckets["vessels"].append(decl)
        elif kind == "StageDecl":
            buckets["stages"].append(decl)
        elif kind == "FlowDecl":
            buckets["flows"].append(decl)
        elif kind == "BondDecl":
            buckets["bonds"].append(decl)
        elif kind == "SeamDecl":
            buckets["seams"].append(decl)

    return buckets


def _format_certainty(certainty):
    if not certainty:
        return "unknown"
    return getattr(certainty, "kind", str(certainty))


def _format_stakes(stakes):
    if not stakes:
        return "medium"
    return getattr(stakes, "level", str(stakes))


