# Backends that support compilation to executable
COMPILABLE_BACKENDS = ['python-tk', 'pyqt5', 'pyqt6', 'pyside6', 'python-qt']


def cmd_compile(args):
    """Compile a GUILDS spec to a standalone executable."""
    spec_path = Path(args.spec)
    if not spec_path.exists():
        print(f"Error: File not found: {spec_path}")
        return 1

    # Check if PyInstaller is available
    try:
        import PyInstaller.__main__
    except ImportError:
        print("Error: PyInstaller is not installed.")
        print("Install it with: pip install pyinstaller")
        return 1

    backend = args.backend

    # Validate backend supports compilation
    if backend not in COMPILABLE_BACKENDS:
        print(f"Error: Backend '{backend}' does not support compilation to executable.")
        print(f"Supported backends: {', '.join(COMPILABLE_BACKENDS)}")
        return 1

    name = spec_path.stem
    out_dir = Path(args.output) if args.output else Path('outputs') / name

    print(f"Compiling {spec_path} with backend '{backend}'...")

    # Step 1: Generate Python source
    try:
        source = spec_path.read_text(encoding='utf-8')
        program, violations = parse_source(source, name)
    except (LexError, ParseError) as e:
        print(f"Parse error: {e}")
        return 1

    # Check for errors
    errors = [v for v in violations if v.severity == "error"]
    if errors:
        print(f"Specification has {len(errors)} error(s):")
        for e in errors[:5]:
            print(f"  - {e}")
        return 1

    # Evaluate using evaluator phase constants (string names bypass stage phase configs).
    ctx = EvalContext(phase=_phase_to_const("idle", default="idle"), now=time.time())
    evaluator = Evaluator(program, ctx)
    surface = evaluator.evaluate()

    # Build with the specified backend
    from guilds_renderers import get_renderer
    from guilds_renderer import RenderTreeBuilder

    builder = RenderTreeBuilder(surface, evaluator)
    tree = builder.build()

    renderer = get_renderer(backend)

    # Convert tree if needed (renderer may expect different format)
    from guilds_renderers.base import RenderTree as NewRenderTree, RenderNode as NewRenderNode, RenderStyle

    def convert_node(old_node):
        style = RenderStyle(
            is_dominant=getattr(old_node.style, 'is_dominant', False) if hasattr(old_node, 'style') else False,
            opacity=0.6 if old_node.visibility == "fade" else 1.0,
        )
        # Convert kind to string if it's an enum
        kind = old_node.kind
        if hasattr(kind, 'name'):
            # Enum - use the name
            kind = kind.name.lower()
        elif isinstance(kind, str):
            kind = kind.lower()
        else:
            kind = str(kind).lower()

        new_node = NewRenderNode(
            name=old_node.name,
            kind=kind,
            visibility=old_node.visibility,
            style=style,
            label=old_node.label,
            subtitle=getattr(old_node, 'subtitle', None),
            certainty=getattr(old_node, 'certainty', None),
            flow_state=getattr(old_node, 'flow_state', None),
            active_failure=getattr(old_node, 'failure', None) or getattr(old_node, 'active_failure', None),
            children=[convert_node(c) for c in old_node.children],
            meta={}
        )
        return new_node

    new_tree = NewRenderTree(
        roots=[convert_node(r) for r in tree.roots],
        phase=tree.phase_name,
        phase_name=tree.phase_name,
        phase_colour=tree.phase_colour,
        lambda_omega=tree.lambda_omega,
        budget_warnings=[],
        symbol_errors=[],
        active_failures=[]
    )

    # Render Python code
    code = renderer.render(new_tree, app_name=name.title().replace('_', '').replace('-', ''))

    # Write Python source
    out_dir.mkdir(parents=True, exist_ok=True)
    output_files = renderer.output_files(name)
    py_file = out_dir / output_files[0]
    py_file.write_text(code, encoding='utf-8')
    print(f"Generated Python source: {py_file}")

    # Step 2: Compile with PyInstaller
    dist_dir = out_dir / 'dist'
    build_dir = out_dir / 'build'
    spec_file = out_dir / f'{name}.spec'

    # Determine icon (use default if not specified)
    icon_arg = []
    if args.icon and Path(args.icon).exists():
        icon_arg = ['--icon', args.icon]

    # PyInstaller arguments
    pyinstaller_args = [
        str(py_file),
        '--name', args.name or name,
        '--distpath', str(dist_dir),
        '--workpath', str(build_dir),
        '--specpath', str(out_dir),
        '--noconfirm',
    ]

    # Windowed mode (no console) for GUI apps
    if not args.console:
        pyinstaller_args.append('--windowed')

    # One file or one directory
    if args.onefile:
        pyinstaller_args.append('--onefile')

    # Add icon if specified
    pyinstaller_args.extend(icon_arg)

    # Hidden imports for Qt
    if backend in ['pyqt5', 'python-qt']:
        pyinstaller_args.extend(['--hidden-import', 'PyQt5.sip'])
    elif backend == 'pyqt6':
        pyinstaller_args.extend(['--hidden-import', 'PyQt6.sip'])

    print(f"\nRunning PyInstaller...")
    print(f"  Args: {' '.join(pyinstaller_args)}")

    try:
        import PyInstaller.__main__
        PyInstaller.__main__.run(pyinstaller_args)
    except Exception as e:
        print(f"PyInstaller error: {e}")
        return 1

    # Find the output executable
    if args.onefile:
        exe_name = (args.name or name) + ('.exe' if sys.platform == 'win32' else '')
        exe_path = dist_dir / exe_name
    else:
        exe_name = (args.name or name) + ('.exe' if sys.platform == 'win32' else '')
        exe_path = dist_dir / (args.name or name) / exe_name

    if exe_path.exists():
        print(f"\n{'='*60}")
        print(f"Successfully compiled executable!")
        print(f"  Output: {exe_path}")
        print(f"  Size: {exe_path.stat().st_size / 1024 / 1024:.1f} MB")
        print(f"{'='*60}")
    else:
        print(f"\nCompilation completed. Check {dist_dir} for output.")

    return 0


def cmd_pdf(args):
    """Generate a formatted PDF from a markdown, text, or JSON file."""
    try:
        from core.scripts.pdf_generator import generate_pdf_from_file, create_pdf_generator_app
    except ImportError as e:
        print(f"Error importing PDF generator: {e}")
        print("Install PDF support with: pip install -e core[pdf]")
        print("For the GUI utility, use: pip install -e core[pdf-gui]")
        return 1

    if getattr(args, 'gui', False):
        try:
            app_cls = create_pdf_generator_app(
                initial_input=(getattr(args, 'input_path', None) or args.input),
                initial_output=args.output,
            )
        except RuntimeError as e:
            print(f"Error launching PDF GUI: {e}")
            return 1
        app_cls().run()
        return 0

    input_path = getattr(args, 'input_path', None) or args.input
    if not input_path:
        print("Error: input is required unless you use --gui")
        return 1

    if not os.path.exists(input_path):
        print(f"Error: {input_path} not found")
        return 1

    try:
        output = generate_pdf_from_file(input_path, args.output)
    except Exception as e:
        print(f"Error generating PDF: {e}")
        return 1

    print(f"Generated PDF: {output}")
    return 0


