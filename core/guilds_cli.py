def export_event_schema(program, name, out_dir):
    """Export event schema for integration with other programs."""
    decls = _collect_declarations(program)

    events = {
        "name": name,
        "version": "1.0",
        "phases": ["idle", "orient", "execute", "verify", "integrate", "recover"],
        "events": {
            "incoming": [],
            "outgoing": []
        },
        "actions": [],
        "claims": []
    }

    # Extract affordances as actions
    for afford in decls["affords"]:
        action = {
            "name": afford.name,
            "trigger": "user_action",
            "contracts": len(afford.contracts) if afford.contracts else 0
        }
        events["actions"].append(action)
        events["events"]["outgoing"].append({
            "type": f"action:{afford.name}",
            "payload": {"action": afford.name}
        })

    # Extract claims
    for claim in decls["claims"]:
        c = {
            "name": claim.name,
            "certainty": _format_certainty(claim.certainty),
            "stakes": _format_stakes(claim.stakes)
        }
        events["claims"].append(c)

    # Common incoming events
    events["events"]["incoming"] = [
        {"type": "phase:change", "payload": {"phase": "string"}},
        {"type": "data:update", "payload": {"claim": "string", "value": "any"}},
        {"type": "failure:inject", "payload": {"kind": "string", "vessel": "string"}}
    ]

    out_file = out_dir / f"{name}_events.json"
    with open(out_file, 'w', encoding='utf-8') as f:
        json.dump(events, f, indent=2)

    print(f"Exported event schema: {out_file}")
    print(f"\nIntegration example:")
    print(f'''
  // In your application:
  const guildsFrame = document.getElementById('guilds-frame');

  // Send events to GUILDS UI
  guildsFrame.contentWindow.postMessage({{
    type: 'phase:change',
    payload: {{ phase: 'execute' }}
  }}, '*');

  // Receive events from GUILDS UI
  window.addEventListener('message', (event) => {{
    if (event.data.type?.startsWith('action:')) {{
      console.log('User action:', event.data);
    }}
  }});
''')


def export_state_machine(program, name, out_dir):
    """Export flows as state-machine artifacts."""
    decls = _collect_declarations(program)
    flows = decls["flows"]

    def mermaid_id(value):
        cleaned = "".join(ch if ch.isalnum() else "_" for ch in str(value))
        cleaned = cleaned.strip("_")
        return cleaned or "state"

    machines = []
    for flow in flows:
        states = []
        transitions = []

        for index, step in enumerate(flow.steps):
            state = {
                "id": step.name,
                "kind": step.state,
                "duration": getattr(step.duration, "kind", str(step.duration)),
                "claim": getattr(step.claim, "name", None) if hasattr(step.claim, "name") else (step.claim if isinstance(step.claim, str) else None),
                "affordances": list(step.affordances),
                "exit_condition": step.exit_cond,
            }
            states.append(state)

            if index + 1 < len(flow.steps):
                transitions.append({
                    "from": step.name,
                    "to": flow.steps[index + 1].name,
                    "trigger": step.exit_cond or "next",
                })

        if flow.terminal and flow.steps:
            for terminal in flow.terminal:
                transitions.append({
                    "from": flow.steps[-1].name,
                    "to": terminal,
                    "trigger": "terminal",
                })

        machines.append({
            "name": flow.name,
            "trigger": getattr(flow.trigger, "kind", str(flow.trigger)),
            "states": states,
            "transitions": transitions,
            "stall": {
                "threshold": getattr(getattr(flow.on_stall, "threshold", None), "kind", None),
                "recovery": list(getattr(flow.on_stall, "recovery", []) or []),
            } if flow.on_stall else None,
        })

    payload = {
        "name": name,
        "version": "1.0",
        "machines": machines,
    }

    json_file = out_dir / f"{name}_statemachine.json"
    with open(json_file, 'w', encoding='utf-8') as f:
        json.dump(payload, f, indent=2)

    mermaid_lines = ["stateDiagram-v2"]
    if not machines:
        mermaid_lines.append("    [*] --> NoFlowsDeclared")
    else:
        for machine in machines:
            mermaid_lines.append(f"    state {mermaid_id(machine['name'])} {{")
            if machine["states"]:
                mermaid_lines.append(f"        [*] --> {mermaid_id(machine['states'][0]['id'])}")
            for transition in machine["transitions"]:
                mermaid_lines.append(
                    f"        {mermaid_id(transition['from'])} --> {mermaid_id(transition['to'])}: {transition['trigger']}"
                )
            mermaid_lines.append("    }")

    mermaid_file = out_dir / f"{name}_statemachine.mmd"
    with open(mermaid_file, 'w', encoding='utf-8') as f:
        f.write("\n".join(mermaid_lines) + "\n")

    print(f"Exported state machine JSON: {json_file}")
    print(f"Exported state machine Mermaid: {mermaid_file}")
    print(f"  Flows exported: {len(machines)}")


